#include <iostream>
using namespace std;

// 1. Declare an integer variable named n.
// 2. Initialize n to a value of your choice.
// 3. Write a for loop that counts from 1 to n, printing each number to the console.

int main()
{
    int n;
    n = 10;

    for (int i = 1; i <= n; i++)
    {
        cout << i << endl;
    }

    return 0;
}